package oopbasic;

public
class Numbers {
    public static
    int plus ( int number1, int number2 ) {
        return number1 + number2;
    }

    public static
    int minus ( int number1, int number2 ) {
        return number1 - number2;
    }

    public static
    int multiply ( int number1, int number2 ) {
        return number1 * number2;
    }

    public static int divide(int number1,int number2){
        return number1/number2;
    }
}
